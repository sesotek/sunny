    <h1 class="main">Gnomonic Thoughts</h1>
<div class="main_text">
<p>
A sundial is a device that indicates the time using the shadow cast by the sun as it moves through the sky over a day. They can take myriad forms, ranging from fold-up portable models to gigantic monumental structures. Sundials have a long and rich history, extending back to the second millenium BC in Egypt. Until reliable watches and clocks became common in the 19th century, the time of day was what the sundial said. Any educated person was well-versed in their principles of operation and construction. Although their purpose was practical, they were also often designed with strong aesthetics and with moral principles in mind. Today we have superior ways of keeping time to split-second accuracy, but the allure of the sundial has not faded. It is still a powerful means of indicating time.
</p>
<p>
A well-designed sundial has many effects on viewers, even in this new millenium. Perhaps the most profound are those of a philosophical nature, in which this device that records time ironically engages the viewer in a timeless manner. And perhaps it is precisely because of our high-tech world that we need to heed Ralph Waldo Emerson's words long ago:
</p>
<div class="quote">
<p>
The civilized man has built a coach, but has lost the use of his feet. He is supported on crutches, but lacks so much support of muscle. He has a fine Geneva watch, but he fails of the skill to tell the hour by the sun. The solstice he does not observe; the equinox he knows as little; and the whole bright calendar of the year is without a dial in his mind.....His note-books impair his memory; his libraries overload his wit; the insurance office increases the number of accidents; and it may be a question whether machinery does not encumber....
</p>
<p>
                            [Self-Reliance, 1841]
</p>
</div>
<p>
Although the principles of a sundial are universal, its design is completely specific to a site and intimately conjoins the viewer, the locale, and the heavens. Although the dial is stationary (no moving parts!), it records fleeting time. Although the dial is passive in character, it speaks strongly and leads to contemplation - of the sun, of the cosmos and our place in it, of time and its passing, of history and mortality.
</p>
<p>
There are many other ways sundials affect us. We can admire them for their beauty and craftmanship and historical value, even if it's raining! We can be fascinated by their technical details and read the time and many other parameters to high accuracy. And a well-designed sundial displays a marvellous mapping of positions and events on the celestial sphere, based on the relationships between the site, the dial surface, and the ever-changing path of the sun. The student of the cosmos directly experiences the passage of days and of seasons, as if the sun were writing a story with its shadow.
</p>
<p>
                - Woody Sullivan (1994)
</p>

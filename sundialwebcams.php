<h3>University of Wisconsin-River Falls, Wisconsin, USA (inactive)</h3>

Designed by John Shepherd in 1994, the Richard D. Swenson dial (on the Kleinpell Fine Arts Building) is a very large dial on a due-south wall; a full description can be found <a href="http://www.uwrf.edu/AboutUs/RichardSwensenSundial.cfm">here</a>. The webcam operated over 1998-2004, but no time-lapses are extant.


<h3>EarthDial Project (inactive, but animations available)</h3>

A flurry of sundial webcams existed in 2004-05 when the <a href="http://planetary.org/programs/projects/earthdial/">EarthDial Project</a>, the <a href="http://www.planetary.org/programs/projects/earthdial/history.html">invention of Bill Nye and Woody Sullivan</a>, was run under the auspices of The Planetary Society. The Project was run in conjunction with the <a href="http://planetary.org/programs/projects/marsdial/">MarsDial Project</a>, which involved the Mars Exploration Rovers Spirit and Opportunity, each of which landed on Mars in 2004 with a small MarsDial on its deck. For the EarthDial Project persons around the world were urged to build sundials of a standard design under a webcam. These images were then made available on a single website (the user clicked on a map to see the individual real-time images). At its peak in late 2004, about 20 EarthDials were operating well in countries not only in Europe and North America, but also in Brazil, Honduras, Malaysia, Israel, and Australia -- for a short time one even existed at the South Pole Research Station! Altogether about <a href="http://www.planetary.org/programs/projects/earthdial/network.html">40 EarthDials</a> participated in the Project. Several sites made animations of the shadows cast by their EarthDial gnomons at different seasons, etc. Here are some examples:

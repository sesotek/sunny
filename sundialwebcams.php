<h3>Pembroke College, Cambridge University, UK (inactive)</h3>

Designed by Frank King in 1997, the Pembroke College wall dial (in the Foundress Court) soon came under the watchful eye of an <a href="http://www.cl.cam.ac.uk/research/dtg/attarchive/sundial">unrelated webcam</a> that was based in a nearby computer science research lab. There were no animations produced, but the setup had the unique feature of employing image-processing software that actually read the dial shadow and then displayed the measured (solar) time! The webcam operated from 1998 to ~2003. The image below is an example.

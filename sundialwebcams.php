<h3>Skyscraper as gnomon in Queens, New York City, USA </h3>

This animation is more an <a href="http://licsundial.net/?page_id=2">art project</a> than a sundial, but is worth listing here. Over 9 hours on 13 May 2007  artist Heidi Neilson followed, as well as she could in an urban environment, the tip of the shadow of a 200-meter (660-ft)-tall skyscraper, taking a photo about every two minutes, looking back up at the building's top.

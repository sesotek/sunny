<p>
Dimensions:     9 x 6 m (~30 x 20 ft)<br>
Gnomon:         6.9 ft long w/ 7-inch-diam. ball (2.1 m, 18 cm)<br>
Materials:      painted aluminum rods, bars, and narrow curved sheet<br>
Mounting:       41 stainless steel bolts<br>
Accuracy: 0.5 minute (corresponding typically to ~1/4 inch = 6 mm; see plot below based on careful reading of the dial over an entire year and at many times of the day)<br>
Design goal:    50 years before maintenance needed<br>
</p>

	    <a href="images/UW_dial_accuracy_plot.gif"><img class="main_img" src="images/UW_dial_accuracy_plot.gif" width="400" alt="Dial accuracy"/></a>
<p>
Vertical or wall sundials are usually painted and often have an associated mural. This wall dial, however, is made of pieces of metal mounted on the brick wall (actually, the mounting bolts go through the brick wall to the concrete wall behind it).</p> 

<p>
The initial idea for the dial came about through conversations between the architects for the Physics/Astronomy Building and Woody Sullivan in 1991-92. The dial was fabricated in the Physics Department Instrument Shop by Larry Stark and installed on the building in March 1994, six months before the building opened.</p> 

	    <img class="main_img" src="images/sundialconstruction.jpg" width="600" alt="Sundial construction" />
<p>
The dial is unique in that its design in fact incorporates two dial planes. The shadow of the tube should be read on the brick wall, but the shadow of the ball is designed to be read on top of the metal surfaces (about 2-3 inches (5-7 cm) from the wall). The dial also has an interesting feature to protect the gnomon from oscillations that might be set up by high winds. The tube and the ball (see photos of Larry (below) and Woody installing) are each half-filled with mineral oil to dampen out any oscillations that might get started. (The chance of this is small, but real - remember that the famous Tacoma Narrows Bridge disaster in 1940 happened close to here!). Another hidden aspect of the dial concerns the sets of holes in the hour lines - they allow one to read the number of hours of daylight on the day of viewing (varying between 16 and 8 hours at our latitude).</p>

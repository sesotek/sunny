    <h1 class="main">Construction of the Sundial</h1>
<div class="main_text">
<p>
<a href="http://www.astro.washington.edu/users/woody/">Prof. Woody Sullivan</a> (project head, designer)<br>
Larry Stark (fabricator)<br>
Mihaly Turbucz (architect, Cesar Pelli firm)<br>
